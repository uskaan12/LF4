package bz.berufsschule.static_methods;

public class taschenrechner {
    //Eine Methode um zwei Werte zu addieren
    public static double taschenrechner_addition(int num1, int num2){
        return num1 + num2;
    }
    //Eine Methode um zwei Werte zu subtrahieren
    public static double taschenrechner_subtraction(int num1, int num2){
        return num1 - num2;
    }

    //Eine Methode um zwei Werte zu multiplizieren
    public static double taschenrechner_multiplikation(int num1, int num2){
        return num1 * num2;
    }
    //Eine Methode um zwei Werte zu dividieren
    public static double taschenrechner_division(int num1, int num2){
        return num1 % num2;
    }
    //Eine Main Methode in der der Benutzer Werte eingibt
}
