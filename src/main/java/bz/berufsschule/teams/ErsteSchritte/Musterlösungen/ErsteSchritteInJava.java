/*
Ein Java-datei beginnt immer mit public class, wobei die Klasse den 
selben Namen wie die .java Datei haben muss!
Innerhalb der Klasse definieren wir die sogenanne Main-Methode. Diese
Methode ist der Anfangspunkt eines jeden Programmes und hat den folgenden
Aufbau:

    public static void main(String[] args){}

Achtung: die Main-Methode muss zwischen den geschwungenen Klammer
         der Klasse eingefügt werden!!!
         In NetBeans können wir die Main-Methode einfügen lassen indem
         man main schreibt und dann die Tabulator-Taste drückt

Zwischen den geschwungen Klammer der Main-Methode wird dann der Code
geschrieben, der ausgeführt werden soll.
*/

public class ErsteSchritteInJava {//Anfang der Klasse

    public static void main(String[] args) {//Anfang der Main-Methode
        
        
        System.out.println("Hello World!");//In Java endet jede Anweisung 
                                           // mit einem ; (Semicolon)
        
        //System.out.println ist die Anweisung um etwas in der 
        //Konsole auszugeben. println bedeutet, dass nach der Ausgabe
        //eine neue Zeile gemacht wird.
        
    }//Ende der Main-Methode

    
}//Ende der Klasse und gleichzeitig das Ende der Java-Datei d.h. danach
 //kann kein Code mehr eingefügt werden.
